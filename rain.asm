ORG 00H
MOV P1,#0FFH
MOV P3,#0FFH
MOV P2,#00H
CLR P2.6
SETB P2.4
START:

NEXT:	JB P1.0,NEXT
	CALL DELAY
	CALL BUZZ

BUZZ:
	CPL P2.6
	CPL P2.4
	CALL DELAY1S
RESETT:	JB P3.2, BUZZ
	CLR P2.6
	SETB P2.4
	JMP START
DELAY1S:
	PET:MOV R1,#124
    	MOV R2,#154
    	MOV R3,#8
MEG:	DJNZ R1,MEG
    	DJNZ R2,MEG
    	DJNZ R3,MEG
	RET
DELAY:
PET1: 	MOV R1,#22
    	MOV R2,#134
    	MOV R3,#2
MEG1:	DJNZ R1,MEG1
	DJNZ R2,MEG1
    	DJNZ R3,MEG1
	RET
	END